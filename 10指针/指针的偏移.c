#include<stdio.h>

//指针即地址，对指针的加减称为指针的偏移
//加是向后偏移
//减是向前偏移 
int main()
{
	int a[5]={1,2,3,4,5};
	//数组名 a 的类型是数组，a里边存了一个值，是地址值，是数组的起始地址 
	int* p;//对一个指针变量取值，得到的类型是其基类型
	//int* p;基础类型是int 
	p = a; 
	printf("*p=%d\n",*p); //数组的起始地址对应的值是1所以*p=1
	
	/*****用指针偏移输出数组中的所有元素******/
	int i;
	for(i=0;i<5;i++)
	{
	printf("%d\n",*(p+i));
	} 
	return 0;
} 
