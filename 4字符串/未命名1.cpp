#include<stdio.h>

int main()
{
//	char c ='a';
//	c = c - 32;
//	printf("%c\n",c);//以字符形式来输出c 
	int i = 5;
	float  j = i / 2;
	printf("%f\n",j);//输出结果为2.000
//	因为i被定义为整型变量，所以结果为整型，若要输出结果为2.5应该在计算时将i强制转换为浮点型 
	float k = (float)i/2;//（ ) + 数据类型称为强制类型转换符 
	//强制转换并不是将i由整型转换为浮点型，i依旧还是整型，只是 (float）i这个表达式被强制转换成浮点类型 由5变为5.0了 
	printf("%f\n",k);//输出结果为2.500 
 } 
