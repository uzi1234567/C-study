#include<stdio.h>
//for(表达式1，表达式2，表达式3)语句；
//for循环语句的执行过程
//*1 先求解表达式1
//*2求解表达式2若其值为真(值为非0)则先执行for语句中指定的内嵌语句后执行第(3)步 
//若其值为假(值为0)，则循环结束转到第(5)步 
//*3求解表达式(3)接着判断表达式(2) 
//*4转回第(2)步继续执行
//*5循环结束 

int main()
{
	int i, total;
	for(i=1,total=0;i<=100; i++)
	{
		total = total + i;
	}
	printf("total=%d\n",total);
}
